#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*a;

	a = (unsigned char *)b;
	while (len)
	{
		*a = (unsigned char)c;
		len--;
		a++;
	}
	return (b);
}
